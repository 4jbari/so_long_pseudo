#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*string;

	i = 0;
	if (!s || !f)
		return (0);
	string = malloc((ft_strlen(s) * sizeof(char)) + 1);
	if (!string)
		return (0);
	while (s[i])
	{
		string[i] = f(i, s[i]);
		i++;
	}
	string[i] = '\0';
	return (string);
}
